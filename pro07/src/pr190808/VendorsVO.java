package pr190808;

public class VendorsVO {
	private String vend_id;
	private String vend_name;
	private String vend_address;
	private String vend_city;
	private String vend_state;
	private String vend_zip;
	private String vend_country;
	
	String getVend_id() {
		return vend_id;
	}
	void setVend_id(String vend_id) {
		this.vend_id = vend_id;
	}
	String getVend_name() {
		return vend_name;
	}
	void setVend_name(String vend_name) {
		this.vend_name = vend_name;
	}
	String getVend_address() {
		return vend_address;
	}
	void setVend_address(String vend_address) {
		this.vend_address = vend_address;
	}
	String getVend_city() {
		return vend_city;
	}
	void setVend_city(String vend_city) {
		this.vend_city = vend_city;
	}
	String getVend_state() {
		return vend_state;
	}
	void setVend_state(String vend_state) {
		this.vend_state = vend_state;
	}
	String getVend_zip() {
		return vend_zip;
	}
	void setVend_zip(String vend_zip) {
		this.vend_zip = vend_zip;
	}
	String getVend_country() {
		return vend_country;
	}
	void setVend_country(String vend_country) {
		this.vend_country = vend_country;
	}
	
	
}
